num1 = input("Give me your number")
num2 = input("Give me your number")
operator = input("Give me your operator")

if operator == "+":
    print(f"{num1}" + {num2})
if operator -- "-":
    print(f"{num1} - {num2}")
if operator == "*":
    print(f"{num1} * {num2 }")
if operator == "/":
    print(f"{num1} / {num2}")